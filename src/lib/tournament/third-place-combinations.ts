function normalizeGroupCode(groupCode: string) {
  return groupCode.trim().toUpperCase().replace(/^3/, "");
}

function getGroupSetKey(groupCodes: string[]) {
  return [...new Set(groupCodes.map(normalizeGroupCode))]
    .sort((left, right) => left.localeCompare(right, "es-AR"))
    .join("");
}

function codeToCombination(
  [option, code]: readonly [number, string],
): FifaAnnexCCombination {
  const values = code.split("");
  const slots = Object.fromEntries(
    FIFA_ANNEX_C_SLOTS.map((slot, index) => [slot, values[index]]),
  ) as Record<FifaAnnexCSlot, string>;

  return {
    groups: getGroupSetKey(values).split(""),
    option,
    slots,
  };
}

export const FIFA_ANNEX_C_COMBINATIONS =
  FIFA_ANNEX_C_CODES.map(codeToCombination);

const COMBINATIONS_BY_GROUP_SET = new Map(
  FIFA_ANNEX_C_COMBINATIONS.map((combination) => [
    combination.groups.join(""),
    combination,
  ]),
);

export function findFifaAnnexCCombination(
  groupCodes: string[],
): FifaAnnexCCombination | null {
  const key = getGroupSetKey(groupCodes);

  if (key.length !== 8) {
    return null;
  }

  return COMBINATIONS_BY_GROUP_SET.get(key) ?? null;
}
